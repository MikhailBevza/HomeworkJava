package org.example.homework3.cw3;

public class Books {
    String title;
    String lastname;
    Double price;
    Integer yearOfPublishing;
    Integer numberOfPages;

    public Books(String title, String lastname, Double price, Integer yearOfPublishing, Integer numberOfPages) {
        this.title = title;
        this.lastname = lastname;
        this.price = price;
        this.yearOfPublishing = yearOfPublishing;
        this.numberOfPages = numberOfPages;
    }
}
